package model;

/**
 *
 * @author Dell
 */
public class TotalIncidents {

    private int year;
    private int total;
    private int idTiquete;

    public TotalIncidents(int year, int total, int idTiquete) {
        this.year = year;
        this.total = total;
        this.idTiquete = idTiquete;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getIdTiquete() {
        return idTiquete;
    }

    public void setIdTiquete(int idTiquete) {
        this.idTiquete = idTiquete;
    }

}
